package Wedding;

public class Decorations {
    private String decorationOption;

    // Constructor for decorations
    public Decorations(String decorationOption) {
        this.decorationOption = decorationOption;
    }

    // Getter for decorations
    public String getDecorationOption() {
        return decorationOption;
    }

    // Setter for decorations
    public void setDecorationOption(String decorationOption) {
        this.decorationOption = decorationOption;
    }
}

